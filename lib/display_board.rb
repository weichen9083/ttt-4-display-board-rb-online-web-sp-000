# Define display_board that accepts a board and prints
# out the current state.
def display_board(board)
  board[0] = a
  board[1] = b
  board[2] = c
  board[3] = d
  board[4] = e
  board[5] = f
  board[6] = g 
  board[7] = h 
  board[8] = i
  puts " #{a} | #{b} | #{c} "
  puts "-----------"
  puts " #{d} | #{e} | #{f} "
  puts "-----------"
  puts " #{g} | #{h} | #{i} "
end 


